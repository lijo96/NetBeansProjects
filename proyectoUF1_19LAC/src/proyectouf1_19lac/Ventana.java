    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jTextArea1.setEditable(false);
        jTextArea2.setEditable(false);
        jButton1.setEnabled(false);
        dp = new Drop();

        pro1 = new Producer(dp, "Producer 1");
        pro1.addObserver(this);
        pro2 = new Producer(dp, "Producer 2");
        pro2.addObserver(this);
        pro3 = new Producer(dp, "Producer 3");
        pro3.addObserver(this);
        con1 = new Consumer(dp, "Consumer 1");
        con1.addObserver(this);
        con2 = new Consumer(dp, "Consumer 2");
        con2.addObserver(this);
        con3 = new Consumer(dp, "Consumer 3");
        con3.addObserver(this);

        tp1 = new Thread(pro1);
        tp2 = new Thread(pro2);
        tp3 = new Thread(pro3);
        tc1 = new Thread(con1);
        tc2 = new Thread(con2);
        tc3 = new Thread(con3);
        Threads_list.add(tc1);
        Threads_list.add(tc2);
        Threads_list.add(tc3);
        Threads_list.add(tp1);
        Threads_list.add(tp2);
        Threads_list.add(tp3);

        Thread[] ts = {tp1, tp2, tp3, tc1, tc2, tc3};
        this.tc = new TimeCounter(ts);
        this.tc.addObserver(this);
        ttc = new Thread(this.tc);

        ttc.start();

        

        //Comienza a contar en cuanto el botón star es pulsado, incluyendo el tiempo de espera mientras el
        //usuario se decide en cambiar unidades o no
        int respuesta = JOptionPane.showConfirmDialog(this, "19LAC Do you want to modify "
                + "the way you count people"
                + " from units to tens?",
                //Título del JOption
                "19LAC Measurement units",
                JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

        if (respuesta == JOptionPane.YES_OPTION) {
            //cambiar unidades e iniciar el Thread principal
            tc.setCambiarUnidades(true);

        } else {
            //no cambiar unidades e iniciar el Thread pincipal
            tc.setCambiarUnidades(false);
        }
        tc.setInicio(true);

    }//GEN-LAST:event_jButton1ActionPerformed

