    public void update(Observable o, Object arg) {

        if (o.getClass().getSimpleName().equalsIgnoreCase("Consumer")) {
            if (arg != null) {
                java.awt.EventQueue.invokeLater(new Runnable() {
                    public void run() {
                        jTextArea2.append(arg.toString() + "\n");
                    }
                });
            } else {
                interrumpirThreads();
            }
        } else if (o.getClass().getSimpleName().equalsIgnoreCase("Producer")) {
            if (arg != null) {
                java.awt.EventQueue.invokeLater(new Runnable() {
                    public void run() {
                        jTextArea1.append(arg.toString() + "\n");
                    }
                });
            } else {
                interrumpirThreads();
            }
        } else if (o.getClass() == tc.getClass()) {

            //Label donde salen el tiempo de ejecución
            jLabel3.setText(tc.getDuracion() + " miliseconds");
            for (Barco e : Recursos.listBarco) {
                //TextArea de Empleado
                jTextArea3.append("19LAC The boat " + e.getNombreBarco() + " with ID " + e.getIdBarco() + " carried " + e.getNumPasajeros() + " people \n");
            }
        }

    }

    synchronized void interrumpirThreads() {
        for (Thread t : this.Threads_list) {
            if (t.isAlive()) {
                t.interrupt();
                System.out.println("thread: " + t.getName());
            }
        }
    }

