    private void empezarTCP() {
        try {
            ServerSocket socketServidor = new ServerSocket(8092);
            serverTCP = new ServerTCP(socketServidor, this);
            new Thread(serverTCP).start();
        } catch (IOException ex) {
            Logger.getLogger("").log(Level.SEVERE, null, ex);
        } catch (NumberFormatException ex) {
            System.out.println("Port format invalid: " + ex.getMessage());
        }
    }
    
    private void empezarUDP(){
        ServerUDP ms = new ServerUDP();
        ms.registerObserver(this);
        Thread server = new Thread(ms);
        server.start();
    }
    
    private void empezarWeb(){
        new Thread(new ServerWeb()).start();
    }

