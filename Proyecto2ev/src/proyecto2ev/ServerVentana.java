    @Override
    public void update(Subject subject) {
        if (subject instanceof HandlerRecpMsj) {
            HandlerRecpMsj handler = (HandlerRecpMsj) subject;
            jTextArea1.setText(jTextArea1.getText() + handler.msg + "\n");
            Info i = handler.info;
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            Object[] row = {i.getIpL(),
                i.getPortL(),
                i.getFqdnL(),
                i.getNameL(),
                i.getIpR(),
                i.getPortR(),
                i.getFqdnR(),
                i.getNameR(),
                i.getDate()
            };
            model.addRow(row);
            jTable1.setModel(model);
            jTable1.revalidate();
            jTable1.repaint();
        } else if (subject instanceof ServerUDP) {
            jLabel1.setIcon(Icon.cargarImagen(((ServerUDP) subject).getUltimaImg()));
        }
    }

